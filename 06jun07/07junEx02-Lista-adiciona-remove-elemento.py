#1. Começando com a lista numeros = [10, 20, 30]
lista = [10,20,30]

#2. Adicione o número 40 ao final da lista.
lista.append(40)

#3. Depois, remova o número 20 da lista.
lista.remove(20)

#4.Por fim, imprima a lista resultante.
print(lista)